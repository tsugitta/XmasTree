import UIKit

extension UIColor {
    
    class func branchBrownColor() -> UIColor {
        return UIColor(red: 50 / 255, green: 20 / 255, blue: 10 / 255, alpha: 1)
    }
    
    class func leafGreenColor() -> UIColor {
        return UIColor(red: 0, green: 0.1, blue: 0, alpha: 1)
    }
    
}

